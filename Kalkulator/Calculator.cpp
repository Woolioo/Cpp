#include "Calculator.hpp"
#include <cmath>

float add(float x, float y) { 
	return x + y; 
}
float substract(float x, float y) { 
	return x - y; 
}
float multiply(float x, float y) { 
	return x * y; 
}
float divide(float x, float y) { 
	return x / y; 
}
float power(float x, float y) { 
	return pow(x,y); 
}
